    <h2 class="text-2xl font-bold mb-4">¿Qué es una Agencia Matrimonial?</h2>

    <p class="mb-4">Una agencia matrimonial es una organización que se dedica a unir personas que buscan relaciones estables y comprometidas. A diferencia de las aplicaciones de citas o las redes sociales, las agencias matrimoniales ofrecen un servicio personalizado, confidencial y orientado a resultados, con el objetivo de encontrar la pareja más compatible para cada cliente.</p>

